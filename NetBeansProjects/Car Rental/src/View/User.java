     private void loadCurrentBookingsTable() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();  // Assuming jTable1 is your bookings table
        tableModel.setRowCount(0);  // Clear the table

        // Fetch all users from MainUserModel and add them to the table
        for (MainUser user : MainUserModel.getAllUsers()) {
            tableModel.addRow(new Object[]{
                user.getFullName(),
                user.getCarModel(),
                user.getReserveDate()
            });
        }
    }

