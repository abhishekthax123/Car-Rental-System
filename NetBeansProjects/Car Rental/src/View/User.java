    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
 String fullName = jTextField3.getText();  // User's Full Name
    String carModel = jTextField1.getText();  // User's Car Model
    String reserveDate = jTextField2.getText();  // User's Reserve Date

    // Check if any fields are empty
    if (fullName.isEmpty() || carModel.isEmpty() || reserveDate.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill all fields.","Error",JOptionPane.ERROR_MESSAGE);
    return;
}

// Check for duplicate user based on name
if (MainUserModel.exists(fullName)) {
    JOptionPane.showMessageDialog(this, "User already exists.","Error",JOptionPane.ERROR_MESSAGE);
    return;
}

// Add the new user (booking) to the MainUserModel
MainUserModel.addUser(new MainUser(fullName, carModel, reserveDate));

// Reload the user table (Current Bookings Table)
loadCurrentBookingsTable();

// Clear the input fields after adding a booking
clearBookingFields();

// Show success message
JOptionPane.showMessageDialog(this, "Booking successful!");
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         String fullName = jTextField3.getText();  // User's Full Name for cancellation

    // Check if Full Name is provided
    if (fullName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Full Name to cancel the reservation.","Error",JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Delete the user (reservation) from MainUserModel by Full Name
    boolean deleted = MainUserModel.deleteUserByName(fullName);

    // Show confirmation message
    if (deleted) {
        JOptionPane.showMessageDialog(this, "Reservation cancelled successfully.");
        loadCurrentBookingsTable();  // Reload the table after cancellation
    } else {
        JOptionPane.showMessageDialog(this, "Reservation not found.","Error",JOptionPane.ERROR_MESSAGE);
    }

    // Clear the input fields after canceling
    clearBookingFields();
    }//GEN-LAST:event_jButton5ActionPerformed

