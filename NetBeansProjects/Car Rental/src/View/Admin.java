    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
  String id = jTextField1.getText();      
String model = jTextField2.getText();   
String brand = jTextField3.getText();   

// 1. Empty check
if (id.isEmpty() || model.isEmpty() || brand.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill all fields");
    return;
}

// 2. Validate Car ID (must be numeric, positive, non-zero)
int carIdValue = 0;
for (int i = 0; i < id.length(); i++) {
    char c = id.charAt(i);
    if (c < '0' || c > '9') {
        JOptionPane.showMessageDialog(
            this,
            "Car ID must be a valid positive number",
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
        return;
    }
    carIdValue = carIdValue * 10 + (c - '0');
}

if (carIdValue <= 0) {
    JOptionPane.showMessageDialog(
        this,
        "Car ID cannot be zero or negative",
        "Error",
        JOptionPane.ERROR_MESSAGE
    );
    return;
}

// 3. Duplicate check
if (CarModel.exists(id)) {
    JOptionPane.showMessageDialog(
        this,
        "Car already exists",
        "Error",
        JOptionPane.ERROR_MESSAGE
    );
    return;
}

// 4. Queue full check handled inside model
boolean added = CarModel.addCar(new Car(id, model, brand));

if (added) {
    loadCarTable();
    clearCarFields();
    JOptionPane.showMessageDialog(
        this,
        "Car added successfully!",
        "Success",
        JOptionPane.INFORMATION_MESSAGE
    );
} else {
    JOptionPane.showMessageDialog(
        this,
        "Queue is full! Cannot add more cars.",
        "Queue Full",
        JOptionPane.ERROR_MESSAGE
    );
}

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    String carId = jTextField1.getText();

    if (carId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Select a car to delete");
        return;
    }

    boolean deleted = CarModel.deleteCarById(carId);

    if (deleted) {
        JOptionPane.showMessageDialog(this, "Car deleted successfully");
        loadCarTable();
        clearCarFields();
    } else {
        JOptionPane.showMessageDialog(this, "Car not found","Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed

